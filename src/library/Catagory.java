   public void catagoryLode(){
      int cata;
        try {
            pre=con.prepareStatement("select*from catagory");
            rs=pre.executeQuery();
            ResultSetMetaData rsmd=rs.getMetaData();
            cata=rsmd.getColumnCount();
            DefaultTableModel d=(DefaultTableModel)catagorTable.getModel();
            d.setRowCount(0);
            while(rs.next()){
                  Vector v=new Vector();
                  for(int i=1; i<=cata;i++){
                  v.add(rs.getString("Id"));
                  v.add(rs.getString("CatagoryName"));
                  v.add(rs.getString("Status"));        
                  }
                d.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Catagory.class.getName()).log(Level.SEVERE, null, ex);
        }
   } 


   


  
  
  
  
  
  
  
  
  
  
  
  

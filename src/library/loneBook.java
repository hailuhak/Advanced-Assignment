public void loneBookLode() {
    try {
        int var;

        pre = con.prepareStatement("select l.id,m.name,b.bookName,l.loneDate,l.returnDate from lonebook l JOIN member m ON l.memberId=m.id JOIN book b ON l.bookId=b.id ");
         rs = pre.executeQuery();
         ResultSetMetaData rsmd= rs.getMetaData();
         var=rsmd.getColumnCount();

        DefaultTableModel d = (DefaultTableModel) loneTable.getModel();
        d.setRowCount(0);

        while (rs.next()) {
            Vector<String> v = new Vector<>();
            for(int i=1;i<var;i++){
            v.add(rs.getString("l.id"));             
            v.add(rs.getString("m.name"));       
            v.add(rs.getString("b.bookName"));   
            v.add(rs.getString("l.loneDate"));          
            v.add(rs.getString("l.returnDate")); 
        }
            d.addRow(v);
        }

       

    } catch (SQLException ex) {
        Logger.getLogger(loneBook.class.getName()).log(Level.SEVERE, null, ex);
    }
}





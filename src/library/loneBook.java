        String mName=txtMeId.getText();
       bookItem  bookItem = (bookItem)combBook.getSelectedItem();
       
       SimpleDateFormat datef =new SimpleDateFormat("yyy,MM,dd");
       String lDate=datef.format(loneDate.getDate());
       
       SimpleDateFormat date1 =new SimpleDateFormat("yyy,MM,dd");
       String RDate=date1.format(retDate.getDate());
       
       
       
        try {
            pre =con.prepareStatement("insert into lonebook(memberId,bookId,loneDate,returnDate)values(?,?,?,?)");
     
            pre.setString(1,mName);
            pre.setInt(2,bookItem.id);
            pre.setString(3,lDate);
            pre.setString(4,RDate);
            
            int p=pre.executeUpdate();
            
            if(p==1){
                JOptionPane.showMessageDialog(this,"book Issued");

                    txtMeId.setText("");
                    combBook.setSelectedIndex(-1);
                    txtMember.setText("");
                    loneDate.setDate(null);
                    retDate.setDate(null);
                    loneBookLode();
               

                 }else{
                      JOptionPane.showMessageDialog(this,"Errore");

                   }
                  
            
        } catch (SQLException ex) {
            Logger.getLogger(loneBook.class.getName()).log(Level.SEVERE, null, ex);
        }
       
        

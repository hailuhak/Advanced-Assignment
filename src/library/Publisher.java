   public void publisherLode(){
      int publisher;
        try {
            pre=con.prepareStatement("select*from publisher");
            rs=pre.executeQuery();
            ResultSetMetaData rsmd=rs.getMetaData();
            publisher=rsmd.getColumnCount();
            DefaultTableModel d=(DefaultTableModel)publisherTable.getModel();
            d.setRowCount(0);
            while(rs.next()){
                  Vector v=new Vector();
                  for(int i=1; i<=publisher;i++){
                  v.add(rs.getString("id"));
                  v.add(rs.getString("publisherName"));
                  v.add(rs.getString("Address"));    
                   v.add(rs.getString("phonNo"));   
                  }
                d.addRow(v);
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(Publisher.class.getName()).log(Level.SEVERE, null, ex);
        }
   } 
  
